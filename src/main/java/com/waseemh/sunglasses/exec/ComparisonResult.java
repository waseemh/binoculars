package com.waseemh.sunglasses.exec;


public class ComparisonResult {
	
	private String captureName;
	
	private double missmatch;

	public String getCaptureName() {
		return captureName;
	}

	public void setCaptureName(String captureName) {
		this.captureName = captureName;
	}

	public double getMissmatch() {
		return missmatch;
	}

	public void setMissmatch(double missmatch) {
		this.missmatch = missmatch;
	}

}
